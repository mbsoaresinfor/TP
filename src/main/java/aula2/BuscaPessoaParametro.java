package aula2;

public class BuscaPessoaParametro {
	public int idade;
	public String nome;
	public String endereco;
	public int cpf;
	public int rg;

	public BuscaPessoaParametro(int idade, String nome, String endereco, int cpf, int rg) {
		this.idade = idade;
		this.nome = nome;
		this.endereco = endereco;
		this.cpf = cpf;
		this.rg = rg;
	}
}